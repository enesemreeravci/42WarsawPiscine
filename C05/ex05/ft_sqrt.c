#include <stdio.h>

int	ft_sqrt(int nb)
{
	int	num;
	int	i;

	num = 0;
	i = 0;
	while (1)
	{
		num = i * i;
		if (nb == num)
		{
			return (i);
		}
		if (num >= nb)
			break ;
		i++;
	}
	return (0);
}
